import datetime, os, random
from typing import List

def get_copyright_str() -> str:
    return "\u00A9" + f" Generated by Bi_Gu-bot at {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}"

def get_output_path(name: str, temp: bool = False) -> str:
    if temp: return os.getcwd() + f"/src/data/temp/{name}_{datetime.datetime.now().strftime('%Y%m%d_%H%M%S')}.png" 
    return os.getcwd() + f"/src/data/output/{name}_{datetime.datetime.now().strftime('%Y%m%d_%H%M%S')}.png"

def get_data_path(subpath: str = "", temp: bool = False) -> str:
    if temp: return os.getcwd() + f"/src/data/temp/{subpath}"
    return os.getcwd() + f"/src/data/{subpath}"

def get_IO_path(filename: str, file_type: str) -> str:
    return os.getcwd() + f"/src/data/{file_type}s/{filename}.{file_type}"

def get_asset_path(subpath: str) -> str:
    return os.getcwd() + f"/src/assets/{subpath}"

def second_to_hms(seconds, eng: bool = False) -> str:
    h, r = divmod(seconds, 3600)
    m, s = divmod(r, 60)
    return f"{int(h)}小时{int(m)}分钟{int(s)}秒" if not eng else f"{int(h)}h{int(m)}m{int(s)}s"

def random_trigger(prob: int) -> bool:
    return random.randint(1, 100) <= prob

def get_datelist(time_range: str) -> List[str]:
    now = datetime.datetime.now()
    last_day = (now - datetime.timedelta(days = 1) if time_range == "yesterday" else now if time_range in ["week", "month", "year", "today"] 
                else now - datetime.timedelta(days = now.weekday() + 1) if time_range == "last week" 
                else now.replace(day = 1) - datetime.timedelta(days = 1) if time_range == "last month" 
                else None)
    first_day = (last_day if time_range in ["yesterday", "today"] else last_day - datetime.timedelta(days = last_day.weekday()) if time_range in ["week", "last week"] 
                 else last_day.replace(day = 1) if time_range in ["month", "last month"] else last_day.replace(month = 1, day = 1) if time_range == "year" 
                 else None)
    if last_day is None or first_day is None:
        raise ValueError(f"Unsupported time range: {time_range}")
    datelist, current_day = [], first_day
    while current_day <= last_day:
        datelist.append(current_day.strftime("%Y-%m-%d"))
        current_day += datetime.timedelta(days = 1)
    return datelist